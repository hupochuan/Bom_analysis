package indi.ycl.model;

public class Company {
	int id;
	String name;
	String code;
	int tonghua_bigclass;
	int tonghua_smallclass;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getTonghua_bigclass() {
		return tonghua_bigclass;
	}
	public void setTonghua_bigclass(int tonghua_bigclass) {
		this.tonghua_bigclass = tonghua_bigclass;
	}
	public int getTonghua_smallclass() {
		return tonghua_smallclass;
	}
	public void setTonghua_smallclass(int tonghua_smallclass) {
		this.tonghua_smallclass = tonghua_smallclass;
	}
	
	

}
